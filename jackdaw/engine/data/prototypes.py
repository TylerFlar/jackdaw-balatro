    blueprint_compat: bool = True
    perishable_compat: bool = True
    eternal_compat: bool = True
    unlocked: bool = True
    discovered: bool = False
    unlock_condition: dict[str, Any] | None = None
    enhancement_gate: str | None = None
    no_pool_flag: str | None = None
    yes_pool_flag: str | None = None


