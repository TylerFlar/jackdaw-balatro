# ---------------------------------------------------------------------------
# JSON loading helpers
# ---------------------------------------------------------------------------

def _load_json(filename: str) -> dict[str, Any]:
    with open(_DATA_DIR / filename) as f:
        return json.load(f)


def _get(d: dict, key: str, default: Any = None) -> Any:
    """Get with null/missing handling.  Empty lists become empty dicts (Lua {} ambiguity)."""
    v = d.get(key, default)
    if v is None:
        return default
    # Empty Lua tables serialize as [] in JSON; normalize to {} for config dicts
    if isinstance(v, list) and len(v) == 0 and isinstance(default, dict):
        return {}
    return v


