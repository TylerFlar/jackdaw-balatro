# Build lookup dicts
# ---------------------------------------------------------------------------

def _build_centers() -> (
    tuple[
        dict[str, JokerProto],
        dict[str, TarotProto],
        dict[str, PlanetProto],
        dict[str, SpectralProto],
        dict[str, VoucherProto],
        dict[str, BackProto],
        dict[str, BoosterProto],
        dict[str, EnhancementProto],
        dict[str, EditionProto],
    ]
):
    raw = _load_json("centers.json")

    jokers: dict[str, JokerProto] = {}
    tarots: dict[str, TarotProto] = {}
    planets: dict[str, PlanetProto] = {}
    spectrals: dict[str, SpectralProto] = {}
    vouchers: dict[str, VoucherProto] = {}
    backs: dict[str, BackProto] = {}
    boosters: dict[str, BoosterProto] = {}
    enhancements: dict[str, EnhancementProto] = {}
    editions: dict[str, EditionProto] = {}

    for key, d in raw.items():
        s = d.get("set")
        if s == "Joker":
            jokers[key] = JokerProto(
                key=key,
                name=d["name"],
                rarity=d.get("rarity", 1),
                cost=d.get("cost", 0),
                order=d.get("order", 0),
                effect=d.get("effect", ""),
                config=_get(d, "config", {}),
                blueprint_compat=d.get("blueprint_compat", True),
                perishable_compat=d.get("perishable_compat", True),
                eternal_compat=d.get("eternal_compat", True),
                unlocked=d.get("unlocked", True),
                discovered=d.get("discovered", False),
                unlock_condition=d.get("unlock_condition"),
                enhancement_gate=d.get("enhancement_gate"),
                no_pool_flag=d.get("no_pool_flag"),
                yes_pool_flag=d.get("yes_pool_flag"),
            )
        elif s == "Tarot":
            tarots[key] = TarotProto(
                key=key, name=d["name"], cost=d.get("cost", 3),
                order=d.get("order", 0), effect=d.get("effect", ""),
                config=_get(d, "config", {}),
                discovered=d.get("discovered", False),
            )
        elif s == "Planet":
            planets[key] = PlanetProto(
                key=key, name=d["name"], cost=d.get("cost", 3),
                order=d.get("order", 0), config=_get(d, "config", {}),
                discovered=d.get("discovered", False),
            )
        elif s == "Spectral":
            spectrals[key] = SpectralProto(
                key=key, name=d["name"], cost=d.get("cost", 4),
                order=d.get("order", 0), effect=d.get("effect", ""),
                config=_get(d, "config", {}),
                hidden=d.get("hidden", False),
                discovered=d.get("discovered", False),
            )
        elif s == "Voucher":
            req = d.get("requires", [])
            if isinstance(req, str):
                req = [req]
            vouchers[key] = VoucherProto(
                key=key, name=d["name"], cost=d.get("cost", 10),
                order=d.get("order", 0), config=_get(d, "config", {}),
                requires=req, unlocked=d.get("unlocked", True),
                discovered=d.get("discovered", False),
                unlock_condition=d.get("unlock_condition"),
            )
        elif s == "Back":
            backs[key] = BackProto(
                key=key, name=d["name"], order=d.get("order", 0),
                config=_get(d, "config", {}),
                unlocked=d.get("unlocked", True),
                discovered=d.get("discovered", False),
                stake=d.get("stake"),
                unlock_condition=d.get("unlock_condition"),
            )
        elif s == "Booster":
            boosters[key] = BoosterProto(
                key=key, name=d["name"], kind=d.get("kind", ""),
                cost=d.get("cost", 4), order=d.get("order", 0),
                weight=d.get("weight", 1), config=_get(d, "config", {}),
                discovered=d.get("discovered", False),
            )
        elif s == "Enhanced":
            enhancements[key] = EnhancementProto(
                key=key, name=d["name"], order=d.get("order", 0),
                effect=d.get("effect", ""), config=_get(d, "config", {}),
            )
        elif s == "Edition":
            editions[key] = EditionProto(
                key=key, name=d["name"], order=d.get("order", 0),
                config=_get(d, "config", {}),
            )

    return jokers, tarots, planets, spectrals, vouchers, backs, boosters, enhancements, editions


def _build_playing_cards() -> dict[str, PlayingCardProto]:
    raw = _load_json("cards.json")
    result: dict[str, PlayingCardProto] = {}
    for key, d in raw.items():
        rank = d["value"]
        suit = d["suit"]
        result[key] = PlayingCardProto(
            key=key,
            suit=suit,
            rank=rank,
            id=_RANK_TO_ID[rank],
            nominal=_RANK_TO_NOMINAL[rank],
            suit_nominal=_SUIT_NOMINAL[suit],
            face_nominal=1 if rank in _SUIT_IS_FACE else 0,
        )
    return result


def _build_blinds() -> dict[str, BlindProto]:
    raw = _load_json("blinds.json")
    result: dict[str, BlindProto] = {}
    for key, d in raw.items():
        debuff = d.get("debuff", {})
        if isinstance(debuff, list):
            debuff = {}  # empty Lua table serialized as []
        boss = d.get("boss")
        if isinstance(boss, list):
            boss = None
        result[key] = BlindProto(
            key=key, name=d["name"], order=d.get("order", 0),
            dollars=d.get("dollars", 0), mult=d.get("mult", 1),
            debuff=debuff, boss=boss,
            boss_colour=d.get("boss_colour"),
        )
    return result


def _build_tags() -> dict[str, TagProto]:
    raw = _load_json("tags.json")
    result: dict[str, TagProto] = {}
    for key, d in raw.items():
        result[key] = TagProto(
            key=key, name=d["name"], order=d.get("order", 0),
            config=_get(d, "config", {}),
            min_ante=d.get("min_ante"),
            requires=d.get("requires"),
            discovered=d.get("discovered", False),
        )
    return result


def _build_stakes() -> dict[str, StakeProto]:
    raw = _load_json("stakes.json")
    return {
        key: StakeProto(
            key=key, name=d["name"], order=d.get("order", 0),
            stake_level=d["stake_level"], unlocked=d.get("unlocked", False),
        )
        for key, d in raw.items()
    }


def _build_seals() -> dict[str, SealProto]:
    raw = _load_json("seals.json")
    return {
        key: SealProto(
            key=key, order=d.get("order", 0),
            discovered=d.get("discovered", False),
        )
        for key, d in raw.items()
    }


def _build_rarity_pools(
    jokers: dict[str, JokerProto],
) -> dict[int, list[str]]:
    pools: dict[int, list[str]] = {1: [], 2: [], 3: [], 4: []}
    for key, j in jokers.items():
        pools[j.rarity].append(key)
    # Sort by order within each rarity (matches game's sort)
    for r in pools:
        pools[r].sort(key=lambda k: jokers[k].order)
    return pools


def _build_center_pools(
    jokers: dict[str, JokerProto],
    tarots: dict[str, TarotProto],
    planets: dict[str, PlanetProto],
    spectrals: dict[str, SpectralProto],
    vouchers: dict[str, VoucherProto],
    backs: dict[str, BackProto],
    boosters: dict[str, BoosterProto],
    enhancements: dict[str, EnhancementProto],
    editions: dict[str, EditionProto],
) -> dict[str, list[str]]:
    """Build set-name → list-of-keys mapping (matches G.P_CENTER_POOLS)."""
    pools: dict[str, list[str]] = {
        "Joker": sorted(jokers, key=lambda k: jokers[k].order),
        "Tarot": sorted(tarots, key=lambda k: tarots[k].order),
        "Planet": sorted(planets, key=lambda k: planets[k].order),
        "Spectral": sorted(spectrals, key=lambda k: spectrals[k].order),
        "Voucher": sorted(vouchers, key=lambda k: vouchers[k].order),
        "Back": sorted(backs, key=lambda k: backs[k].order),
        "Booster": sorted(boosters, key=lambda k: boosters[k].order),
        "Enhanced": sorted(enhancements, key=lambda k: enhancements[k].order),
        "Edition": sorted(editions, key=lambda k: editions[k].order),
        "Tarot_Planet": sorted(
            list(tarots.keys()) + list(planets.keys()),
            key=lambda k: tarots[k].order if k in tarots else planets[k].order,
        ),
    }
    return pools


